# AND
def AND(a, b):
    return a & b

# OR 
def OR(a, b):
    return a | b
# NOT 
def NOT(a):
    return not a

# XOR 
def XOR(a, b):
    return a ^ b 

# BUFFER 
def BUFFER(a):
    return a

# NAND 
def NAND(a, b):
    return not a & b

# NOR 
def NOR(a, b):
    return not a | b

# XNOR 
def XNOR(a, b):
    return not a ^ b

